#
# 要求：
# - 使用for循环和range()函数
# - 使用字符串遍历
# - 输出格式要清晰
# - 添加适当的注释
#
# ============================================================================

print("打印1到20的所有数字")
for i in range(1, 21):
    print(i)
print("-" * 20)
print("打印1到20的所有偶数")
for i in range(1, 21):
    if i % 2 == 0:
        print(i)
print("-" * 20)
print("打印1到20的所有奇数")
for i in range(1, 21):
    if i % 2 != 0:
        print(i)
print("-" * 20)
print("倒序打印20到1")
# 从20开始,步长-1,实现倒序
for i in range(20, 0, -1):
    print(i)
mult = 1
for i in range(1, 11):
    mult = mult * i
print(f"1到10的乘积（阶乘）结果:{mult}\n")
print("-" * 20)
print("统计字符串\n")
str = input("请输入一个字符串:")
search = input("请输入一个想要查找的字符:")
print()
count = 0
for i in str:
    if i == search:
        count = count + 1
print(f"字符{search}出现的次数为:{count}次")


