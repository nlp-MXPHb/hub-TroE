# 1. 提示用户输入一个正整数n
# 2. 使用while循环计算1到n的所有整数的和
# 3. 使用while循环计算1到n的所有偶数的和
# 4. 使用while循环计算1到n的所有奇数的和
# 5. 输出三个计算结果
#
# 要求：
# - 使用while循环
# - 注意循环条件的设置，避免无限循环
# - 使用取余运算符（%）判断奇偶数
