# 作业五：综合应用 - 购物计算器
# ============================================================================
#
# 任务描述：
# 编写一个购物计算器程序，实现以下功能：
# 1. 提示用户输入以下信息：
#    - 商品1名称、单价、数量
#    - 商品2名称、单价、数量
#    - 商品3名称、单价、数量
# 2. 计算每个商品的小计（单价 × 数量）
# 3. 计算所有商品的总价（三个小计相加）
# 4. 假设折扣率为0.9（打9折），计算折扣后的价格
# 5. 输出格式化的购物清单，包括：
#    - 每个商品的名称、单价、数量、小计
#    - 总价（折扣前）
#    - 折扣率
#    - 折扣后价格
#
# 要求：
# - 使用有意义的变量名
# - 添加详细的注释
# - 输出格式要清晰，可以使用分隔线
# - 价格保留两位小数
# - 使用格式化字符串输出
#
# ============================================================================

good1Info = input("请依次输入商品1的名称、单价、数量,并用逗号隔开")
good2Info = input("请依次输入商品2的名称、单价、数量,并用逗号隔开")
good3Info = input("请依次输入商品3的名称、单价、数量,并用逗号隔开")
# 商品信息
name1 = good1Info.split(',')[0].strip()
price1 = float(good1Info.split(',')[1].strip())
num1 = int(good1Info.split(',')[2].strip())
name2 = good2Info.split(',')[0].strip()
price2 = float(good2Info.split(',')[1].strip())
num2 = int(good2Info.split(',')[2].strip())
name3 = good3Info.split(',')[0].strip()
price3 = float(good3Info.split(',')[1].strip())
num3 = int(good3Info.split(',')[2].strip())
# 商品小计
sub1 = price1 * num1
sub2 = price2 * num2
sub3 = price3 * num3
# 总价
total = sub1 + sub2 + sub3
# 折扣
discount = 0.9
result = total * discount
print("            购物清单")
print(f"商品：{name1}  单价：{price1:.2f}  数量：{num1}  小计：{sub1:.2f}")
print(f"商品：{name2}  单价：{price2:.2f}  数量：{num2}  小计：{sub2:.2f}")
print(f"商品：{name3}  单价：{price3:.2f}  数量：{num3}  小计：{sub3:.2f}")
print(f"折扣前总价：{total:.2f} 元")
print(f"折扣率：{discount:.1f}")
print(f"折扣后价格：{result:.2f} 元")

