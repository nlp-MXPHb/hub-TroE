# 作业二：日期时间处理
# ============================================================================
#
# 任务描述：
# 编写一个程序，使用datetime库完成以下任务：
# 1. 获取当前日期和时间
# 2. 格式化显示日期和时间
# 3. 计算两个日期之间的天数差
# 4. 计算某个日期之后N天的日期
# 5. 显示今天是星期几
#
# 要求：
# - 使用datetime库
# - 格式化日期输出
# - 进行日期计算
#
# ============================================================================

from datetime import datetime, timedelta
now = datetime.now()
print(f"当前日期时间{now}" )
print(f"格式化后：{now.strftime("%Y-%m-%d %H:%M:%S")}")

date1 = datetime(2024, 1, 1)
date2 = datetime(2024, 12, 31)
print(f"两个日期相差天数：{(date2 - date1).days}")
future = now + timedelta(days=10)
print("10天后日期：", future.strftime("%Y-%m-%d"))

weekday = ["星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日"]
today = weekday[now.weekday()]
print("今天是：", today)

